from typing import Iterator


def parse(text: str) -> set[complex]:
    return {
        complex(r, i)
        for i, line in enumerate(text.strip().splitlines())
        for r, char in enumerate(line)
        if char == "#"
    }


def _neighbours(z: complex, include: bool = False) -> Iterator[complex]:
    yield z + 1
    yield z + 1j
    yield z - 1
    yield z - 1j
    yield z + 1 + 1j
    yield z - 1 + 1j
    yield z - 1 - 1j
    yield z + 1 - 1j


def animate(cells: set[complex]) -> set[complex]:
    counts = {}
    for cell in cells:
        for neigh in _neighbours(cell):
            counts[neigh] = counts.get(neigh, 0) + 1
    return {c for c, count in counts.items() if count == 3 or (count == 2 and c in cells)}


def solve(text: str) -> Iterator:
    cells = parse(text)
    border = {z for i in range(-1, 100) for z in [i - 1j, i + 100j, -1 + 1j * i, 100 + 1j * i]}
    for _ in range(100):
        cells = animate(cells) - border
    yield len(cells)

    corners = {complex(r, i) for r in [0, 99] for i in [0, 99]}
    cells = parse(text) | corners
    for _ in range(100):
        cells = corners | animate(cells) - border
    yield len(cells)


