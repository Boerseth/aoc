def draw_cells(scr, cells, N) -> None:
    for i in range(0, N, 2):
        for r in range(N):
            bot = 1 * (complex(r, i) in cells)
            top = 2 * (complex(r, i + 1) in cells)
            char = " .':I"[bot + top]
            scr.addstr(i // 2, r, char)
    scr.refresh()


def main():
    import curses
    import os
    import time

    src = curses.initscr()
    curses.noecho()
    curses.cbreak()

    cells = parse(ex)
    size = os.get_terminal_size()
    dim = min(size.columns, size.lines * 2)
    try:
        draw_cells(src, cells, dim)
        for _ in range(100):
            time.sleep(0.1)
            cells = animate(cells)
            draw_cells(src, cells, dim)
    finally:
        curses.echo()
        curses.nocbreak()
        curses.endwin()


if __name__ == "__main__":
    main()
